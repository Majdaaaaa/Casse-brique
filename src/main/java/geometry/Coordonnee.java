package geometry;

public class Coordonnee {
    double x;
    double y;

    public Coordonnee(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public void setX(double x){
        this.x=x;
    }

    public double getX() {
        return x;
    }

    public void setY(double y){
        this.y=y;
    }

    public double getY() {
        return y;
    }

    public void setCoordonnee(double x, double y){
        this.x=x;
        this.y=y;
    }


    
} 